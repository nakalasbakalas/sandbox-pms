type TranslationKey = keyof typeof translations.en

interface I18nContextValue {
  language: Language
  setLanguage: (language: Language) => void
  t: (key: TranslationKey) => string
}

const I18nContext = createContext<I18nContextValue | undefined>(undefined)

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [storedLanguage, setStoredLanguage] = useKV<Language>('app-language', 'en')
  const language: Language = storedLanguage === 'th' ? 'th' : 'en'

  useEffect(() => {
    document.documentElement.lang = language === 'th' ? 'th-TH' : 'en'
  }, [language])

  const value = useMemo<I18nContextValue>(() => ({
    language,
    setLanguage: setStoredLanguage,
    t: (key) => translations[language][key] ?? translations.en[key] ?? key,
  }), [language, setStoredLanguage])

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) {
    throw new Error('useI18n must be used within LanguageProvider')
  }
  return context
}

export function formatBangkokDate(value: Date | string, language: Language, options?: Intl.DateTimeFormatOptions) {
  return new Intl.DateTimeFormat(language === 'th' ? 'th-TH' : 'en-GB', {
    timeZone: 'Asia/Bangkok',
    day: '2-digit',
    month: 'short',
    year: 'numeric',
    ...options,
  }).format(value instanceof Date ? value : new Date(value))
}

export function formatBangkokTime(value: Date | string, language: Language) {
  return new Intl.DateTimeFormat(language === 'th' ? 'th-TH' : 'en-GB', {
    timeZone: 'Asia/Bangkok',
    hour: '2-digit',
    minute: '2-digit',
    hour12: false,
  }).format(value instanceof Date ? value : new Date(value))
}
