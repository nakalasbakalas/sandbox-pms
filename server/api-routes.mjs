export function resolveApiRouteContract(pathname) {
  const contract = API_ROUTE_CONTRACTS.find((route) => route.pattern.test(pathname))
  if (!contract) return null
  return {
    methods: [...contract.methods],
    allow: contract.methods.join(', '),
  }
}
